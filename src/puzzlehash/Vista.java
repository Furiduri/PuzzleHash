    private void btnStartConcuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnStartConcuMouseClicked
        // TODO add your handling code here:
        if (ValidForm()) {
            txtLogConcurrente.setText("");
            txtHASHConcu.setText("");
            lblKeysConcu.setText("");
            lblTimeEndConcu.setText("");
            lblIntentosConcu.setText("");
            SetStartConcurrent(true);
            HiloConcurrente = new SearchHashConcurrente(txtMainText.getText(),
                    txtRegex.getText(),
                    (int) txtMaxIntentosConcu.getValue(),
                    "Hilo Secuencial",
                    (int) txtMaxIntentosConcu1.getValue(),
                    txtLogConcurrente,
                    btnStopConcu,
                    lblIntentosConcu
            );
            HiloConcurrente.start();
        }
    }//GEN-LAST:event_btnStartConcuMouseClicked

    private void btnStopConcuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnStopConcuMouseClicked
        // TODO add your handling code here:
        if (HiloConcurrente != null) {
            try {
                HiloConcurrente.flagTread = false;
                lblKeysConcu.setText(String.valueOf(HiloConcurrente.key));
                txtHASHConcu.setText(HiloConcurrente.HashFinding);
                lblTimeEndConcu.setText(HiloConcurrente.TimerEnd);
                SetStartConcurrent(false);
            } catch (Throwable ex) {
                Logger.getLogger(Vista.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            SetStartConcurrent(false);
        }
    }//GEN-LAST:event_btnStopConcuMouseClicked

