    private boolean ValidForm() {
        if (txtMainText.getText().isEmpty()) {
            JOptionPane.showMessageDialog(pnlContent, "Favor de ingresar un texto incicial!", "Warning", JOptionPane.WARNING_MESSAGE);
            return false;
        } else if (txtRegex.getText().isEmpty()) {
            JOptionPane.showMessageDialog(pnlContent, "Favor de ingresar un texto o exprecion a buscar!", "Warning", JOptionPane.WARNING_MESSAGE);
            return false;
        }

        return true;
    }

    private void SetStartSecuencial(boolean b) {
        btnStartSecu.setEnabled(!b);
        btnStopSecu.setEnabled(b);
        txtMaxIntentos.setEnabled(!b);
        txtMainText.setEnabled(!b);
        txtRegex.setEnabled(!b);
    }

    private void SetStartConcurrent(boolean b) {
        btnStartConcu.setEnabled(!b);
        btnStopConcu.setEnabled(b);
        txtMainText.setEnabled(!b);
        txtRegex.setEnabled(!b);
    }
